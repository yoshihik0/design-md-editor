import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = path.join(root, 'embedded-resources.js');

async function readText(relativePath) {
  return fs.readFile(path.join(root, relativePath), 'utf8');
}

function yamlField(source, name) {
  const match = source.match(new RegExp(`^${name}:\\s*(.+)$`, 'm'));
  return match ? match[1].trim().replace(/^['"]|['"]$/g, '') : '';
}

async function buildTemplates() {
  const templatesRoot = path.join(root, 'design', 'templates');
  const entries = await fs.readdir(templatesRoot, { withFileTypes: true });
  const templates = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const design = `design/templates/${entry.name}/DESIGN.md`;
    const preview = `design/templates/${entry.name}/PREVIEW.md`;
    const designText = await readText(design);
    const previewText = await readText(preview);
    templates.push({
      id: entry.name,
      name: yamlField(designText, 'title') || entry.name,
      description: yamlField(designText, 'description'),
      design,
      preview,
      hasPreview: true,
      designText,
      previewText
    });
  }

  return templates.sort((a, b) => a.name.localeCompare(b.name, 'ja'));
}

const userGuide = await readText('docs/USER-GUIDE.md');

const payload = {
  version: 1,
  defaultPreview: await readText('defaults/PREVIEW.md'),
  previewManual: await readText('docs/PREVIEW-MANUAL.md'),
  userGuide,
  templates: await buildTemplates()
};

// Keep the generated file safe when loaded as a classic script, even if a
// future Markdown document contains a literal closing script tag.
const serialized = JSON.stringify(payload)
  .replace(/</g, '\\u003c')
  .replace(/\u2028/g, '\\u2028')
  .replace(/\u2029/g, '\\u2029');

const output = `// Generated by scripts/build-embedded-resources.mjs. Do not edit by hand.\nwindow.__DMD_EMBEDDED__ = Object.freeze(${serialized});\n`;
await fs.writeFile(outputPath, output, 'utf8');
await fs.writeFile(path.join(root, 'README.md'), userGuide, 'utf8');
console.log(`Generated ${path.relative(root, outputPath)}`);
console.log('Generated README.md from docs/USER-GUIDE.md');
